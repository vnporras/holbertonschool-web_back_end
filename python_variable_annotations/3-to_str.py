#!/usr/bin/env python3
"""
This module provide to_str function to convert a float to
its string representation
"""


def to_str(n: float) -> str:
    """
    Converts a float number to its string representation.

    Parameters:
    n (float): The float number to convert.

    Returns:
    str: The string representation of the float number.
    """
    return str(n)
