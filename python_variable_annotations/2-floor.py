#!/usr/bin/env python3
"""
This module provides a floor function to get the floor value of a float.
"""

import math


def floor(n: float) -> int:
    """
    Returns the floor of the given float number.

    Parameters:
    n (float): The float number to floor.

    Returns:
    int: The floor value of the float number.
    """
    return math.floor(n)
